@pytest.mark.pinned
@pytest.mark.parametrize("name", sorted(_PROC_TEXTURES.keys()))
def test_texture_pinned_hash(name):
    """SHA-256 of the first 64 bytes of each generator's 64-px PNG is pinned.
    Updating these requires deliberate algorithm changes — protects against silent drift."""
    import hashlib
    png = _PROC_TEXTURES[name](64)
    actual = hashlib.sha256(png[:64]).hexdigest()
    assert actual == _TEXTURE_FIRST64_SHA256[name], (
        f"{name}: pin drift\nexpected: {_TEXTURE_FIRST64_SHA256[name]}\nactual:   {actual}"
    )


def test_proc_textures_registry_has_six_entries():
    assert set(_PROC_TEXTURES) == {
        "oak_bark", "pine_bark", "birch_bark",
        "oak_leaf", "pine_needle", "birch_leaf",
    }


def test_proc_textures_callable_returns_bytes():
    for name, gen in _PROC_TEXTURES.items():
        png = gen()
        assert isinstance(png, bytes) and len(png) > 100, f"generator {name} broken"
